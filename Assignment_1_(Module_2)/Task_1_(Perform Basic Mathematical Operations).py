# Taking 2 operands from the user
n1 = int(input("Enter the first number: "))
n2 = int(input("Enter the second number: "))

# Performing basic mathematical operations with the operands
add = n1 + n2
sub = n1 - n2
mul = n1 * n2
div = n1 / n2

# Printing the output as expected
print("Addition: ", add)
print("Subtraction: ", sub)
print("Multiplication: ", mul)
print("Division: ", div)